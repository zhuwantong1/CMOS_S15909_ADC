void HAL_TIM_IC_CaptureCallback(TIM_HandleTypeDef *htim) 
{
    if (htim->Instance == TIM1 && htim->Channel == HAL_TIM_ACTIVE_CHANNEL_1) //捕获上升沿
    {
        if(G_Clk_Rise_Number==0)
        {
            HAL_GPIO_WritePin(ST_GPIO_Port, ST_Pin, GPIO_PIN_SET);//如果是第一个信号沿上升，那就令ST延迟上升
        }
        if(G_Clk_Rise_Number==1)
        {
            HAL_GPIO_WritePin(ST_GPIO_Port, ST_Pin, GPIO_PIN_RESET);//如果是第二个信号沿上升，那就令ST延迟下降
        }
        if(mul_int<mul_int_max)	//在这里完成EX_INT的高低电平输出,上升沿EX_INT发生变化
        {
            if(G_Clk_Rise_Number>1&&G_Clk_Rise_Number<400)
            {
                HAL_GPIO_WritePin(GPIOA, EX_INT_Pin, GPIO_PIN_SET); 	  //拉高ex-int
            }
            else if(G_Clk_Rise_Number>=400&&G_Clk_Rise_Number<1400)
            {
                HAL_GPIO_WritePin(GPIOA, EX_INT_Pin, GPIO_PIN_RESET);  	//拉低ex-int，积分
            }
            else
            {
                HAL_GPIO_WritePin(GPIOA, EX_INT_Pin, GPIO_PIN_SET);  		//拉高ex-int
            }
        }
        else
        {
            if(G_Clk_Rise_Number>1)
            {
                HAL_GPIO_WritePin(GPIOA, EX_INT_Pin, GPIO_PIN_SET);			 //拉高ex-int
            }

        }
        G_Clk_Rise_Number++;

    }
    if (htim->Instance == TIM3 && htim->Channel == HAL_TIM_ACTIVE_CHANNEL_1) //捕获上升沿
    {

        adc_ans[G_Hamamatsu_Trigger_Rise_Number]=HAL_ADC_GetValue(&hadc1);
        if(adc_ans[G_Hamamatsu_Trigger_Rise_Number]>0xea60)
        {
            adc_ans[G_Hamamatsu_Trigger_Rise_Number]=0xea60;
        }
        adc_ans_u8[G_Hamamatsu_Trigger_Rise_Number_U8] = (uint8_t)(adc_ans[G_Hamamatsu_Trigger_Rise_Number] >> 8);//高8位
        adc_ans_u8[G_Hamamatsu_Trigger_Rise_Number_U8+1]=(uint8_t)adc_ans[G_Hamamatsu_Trigger_Rise_Number];
        G_Hamamatsu_Trigger_Rise_Number_U8=G_Hamamatsu_Trigger_Rise_Number_U8+2;
        G_Hamamatsu_Trigger_Rise_Number++;
    }
}		
